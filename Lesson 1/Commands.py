'''
Основные команды Python:

print(Что_Хотим_Вывести) – команда для вывода на экран
input()                  – команда для ввода с клавиатуры
'''

print("Как тебя зовут:", end = ' ') # Выведет на экран сообщение "Как тебя зовут?"
name = input()                      # Будет ждать ввода с клавиатуры и нажатия клавиши Ввод
print("Привет, " + name)            # Выведет на экран сообщение "Привет, " и допишет введённое выше имя